@Table(name = "t_advertise")
public class Advertise extends BaseEntity implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 5216300125493518362L;
	
	private Integer imgId;
	private Integer status;
	private String url;
	private Integer isInner;
	private Integer province;
	private Integer city;
	private Integer area;
	private Integer createrUser;
	private Date createTime;
	private String icon;
	
	public String getIcon() {
		return icon;
	}
	public void setIcon(String icon) {
		this.icon = icon;
	}
	public Integer getImgId() {
		return imgId;
	}
	public void setImgId(Integer imgId) {
		this.imgId = imgId;
	}
	public Integer getStatus() {
		return status;
	}
	public void setStatus(Integer status) {
		this.status = status;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public Integer getIsInner() {
		return isInner;
	}
	public void setIsInner(Integer isInner) {
		this.isInner = isInner;
	}
	public Integer getProvince() {
		return province;
	}
	public void setProvince(Integer province) {
		this.province = province;
	}
	public Integer getCity() {
		return city;
	}
	public void setCity(Integer city) {
		this.city = city;
	}
	public Integer getArea() {
		return area;
	}
	public void setArea(Integer area) {
		this.area = area;
	}
	public Integer getCreaterUser() {
		return createrUser;
	}
	public void setCreaterUser(Integer createrUser) {
		this.createrUser = createrUser;
	}
	public Date getCreateTime() {
		return createTime;
	}
	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}
}
