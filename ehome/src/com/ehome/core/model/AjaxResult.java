package com.ehome.core.model;

/**
 * Ajax请求返回实体对象
 * 
 * @Title:AjaxResult
 * @Description:TODO
 * @author:张钟武
 * @date:2017年1月20日 上午10:59:14
 * @version:
 */
public class AjaxResult {

	/**
	 * @Fields code : 返回请求代码
	 */
	private Object status;

	/**
	 * @Fields info : 返回请求信息
	 */
	private String message;

	/**
	 * @Fields data : 返回请求数据
	 */
	private Object data;

	/**
	 * <p>
	 * Title: 默认构造方法
	 * </p>
	 * <p>
	 * Description: 默认构造方法
	 * </p>
	 */
	public AjaxResult() {
		super();
	}

	/**
	 * <p>
	 * Title: 含参构造方法
	 * </p>
	 * <p>
	 * Description: 含参构造方法
	 * </p>
	 * 
	 * @param code
	 *            返回代码
	 * @param info
	 *            返回信息
	 * @param data
	 *            返回数据
	 */
	public AjaxResult(Object status, String message, Object data) {
		super();
		this.status = status;
		this.message = message;
		this.data = data;
	}

	/**
	 * @Title: getCode
	 * @Description: 获取代码
	 * @return 代码
	 */
	public Object getStatus() {
		return status;
	}

	/**
	 * @Title: setCode
	 * @Description: 设置代码
	 * @param code
	 *            代码
	 */
	public void setStatus(Object status) {
		this.status = status;
	}

	/**
	 * @Title: getInfo
	 * @Description: 获取信息
	 * @return 信息
	 */
	public java.lang.String getMessage() {
		return message;
	}

	/**
	 * @Title: setInfo
	 * @Description: 设置信息
	 * @param info
	 *            信息
	 */
	public void setMessage(java.lang.String message) {
		this.message = message;
	}

	/**
	 * @Title: getData
	 * @Description: 获取数据
	 * @return 数据
	 */
	public java.lang.Object getData() {
		return data;
	}

	/**
	 * @Title: setData
	 * @Description: 设置数据
	 * @param data
	 *            数据
	 */
	public void setData(java.lang.Object data) {
		this.data = data;
	}
}
